    private void txtSolutoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSolutoKeyTyped
     
        char validar = evt.getKeyChar(); 
        
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(null,"Ingresar solamente números");
            
            
        }
            
        
        
    }//GEN-LAST:event_txtSolutoKeyTyped

