    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   
        // Calculo del porcentaje masa masa 
        
        double soluto; 
        
        double disolvente; 
        
        double porM;
        
        soluto = Double.parseDouble(txtSoluto.getText());
        disolvente = Double.parseDouble(txtDisolvente.getText()); 
        
        porM = (soluto/(soluto + disolvente))*100; 
        
        DecimalFormat df = new DecimalFormat("#.000"); 

        lbOut.setText(String.valueOf(df.format(porM)));
        
        
        
        
        
   
        
    }//GEN-LAST:event_jButton1ActionPerformed

